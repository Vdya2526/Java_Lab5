package lab5;
/* Write a number to print the table of 5, first 10 values.*/
public class Program1 {

	public static void main(String[] args) {
		int n = 10;
		int res=1;
		
		
		for(int i=1;i<=n;i++)
		{
			
			res=i*5;
			System.out.println("first 10 table 5 values are"+res);
			
			
		}

	}

}
